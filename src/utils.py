def convert_ced_to_epics_name(name):
    zone_map = {
        '02': '2', '03': '3', '04': '4', '05': '5', '06': '6',
        '07': '7', '08': '8', '09': '9', '10': 'A',
        '11': 'B', '12': 'C', '13': 'D', '14': 'E',
        '15': 'F', '16': 'G', '17': 'H', '18': 'I',
        '19': 'J', '20': 'K', '21': 'L', '22': 'M',
        '23': 'N', '24': 'O', '25': 'P', '26': 'Q'
    }

    linac = name[0:1]
    zone = zone_map[name[2:4]]
    cav = name[5:6]

    return f"R{linac}{zone}{cav}"
